<script>
  const faqItems = document.querySelectorAll(".faq_main_item");

  faqItems.forEach((item) => {
    const question = item.querySelector(".faq_main_question");
    const answer = item.querySelector(".faq_main_answer");
    const icon = item.querySelector(".faq_main_icon");

    question.addEventListener("click", () => {
      const isActive = item.classList.contains("active");

      // Close all
      faqItems.forEach((el) => {
        el.classList.remove("active");
        el.querySelector(".faq_main_answer").classList.remove("active");
        el.querySelector(".faq_main_icon").textContent = "+";
      });

      // Toggle selected
      if (!isActive) {
        item.classList.add("active");
        answer.classList.add("active");
        icon.textContent = "−";
      } else {
        item.classList.remove("active");
        answer.classList.remove("active");
        icon.textContent = "+";
      }
    });
  });

  // ✅ Keep first FAQ open on reload
  window.addEventListener("DOMContentLoaded", () => {
    const firstItem = document.querySelector(".faq_main_item");
    const firstIcon = firstItem.querySelector(".faq_main_icon");
    const firstAnswer = firstItem.querySelector(".faq_main_answer");
    firstItem.classList.add("active");
    firstAnswer.classList.add("active");
    firstIcon.textContent = "−";
  });
</script>
